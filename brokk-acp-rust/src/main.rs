/// Brokk ACP Server -- Rust-based Agent Client Protocol server with
/// auto-discovery for Codex (`~/.codex/auth.json`) and Ollama (local
/// daemon). Just start it -- the model picker is populated from whichever
/// backends are reachable. No flags required for the common case.
#[derive(Parser)]
#[command(name = "brokk-acp", version, about)]
struct Args {
    /// Ollama base URL. Discovery hits `<url>/api/tags`; chat hits
    /// `<url>/v1/chat/completions`. Set to a non-default value for a
    /// remote Ollama or a custom OpenAI-compatible server that mirrors
    /// the `/api/tags` shape.
    #[arg(long, default_value = "http://localhost:11434")]
    ollama_url: String,

    /// Disable Ollama discovery entirely (skips the `/api/tags` probe at
    /// startup and on every `session/new`). Use this when the network
    /// path to localhost is firewalled or the daemon is intentionally
    /// blocked, to avoid the connection-attempt latency.
    #[arg(long)]
    no_ollama: bool,

    /// Disable Codex discovery entirely (skips reading `~/.codex/auth.json`).
    /// `/codex-login` slash commands still work; this only opts out of
    /// auto-discovering models on the ChatGPT subscription or via the
    /// stored OPENAI_API_KEY.
    #[arg(long)]
    no_codex: bool,

    /// Override the default model id for new sessions. Accepts a wire
    /// form (`codex::gpt-5-codex`, `ollama::llama3:latest`) or a bare id
    /// that routes to the preferred backend (Codex if available, else
    /// Ollama). When unset, the first discovered model wins.
    #[arg(long, default_value = "")]
    default_model: String,

    /// Maximum number of tool-calling turns per prompt before the server forces a final text response.
    #[arg(long, default_value_t = 25)]
    max_turns: usize,

    /// Maximum number of sessions to keep resident in memory before the
    /// least-recently-used session is evicted (the on-disk zip is unaffected
    /// and can be reloaded). Set to `0` to disable the cap.
    #[arg(long, default_value_t = 50)]
    max_sessions: usize,

    /// Maximum number of conversation turns retained per session in memory
    /// (sliding window). Older turns are dropped from memory once the cap is
    /// exceeded; the persisted zip retains the full history. Set to `0` to
    /// disable the cap.
    #[arg(long, default_value_t = 50)]
    max_history_turns: usize,

    /// Path to the bifrost binary (or just "bifrost" to look it up on $PATH).
    /// When unset, code-intelligence tools (search_symbols, etc.) are disabled.
    #[arg(long, env = "BROKK_BIFROST_BINARY")]
    bifrost_binary: Option<PathBuf>,

    // ----- Deprecated backward-compat flags --------------------------------
    // Existing editor configs generated by brokk-code (Python TUI) hand
    // these in. We accept them silently so an upgrade-in-place doesn't
    // break the user's IDE; but they no longer drive routing -- Codex and
    // Ollama auto-discover, so the previous semantics ("hardcode this one
    // backend") are obsolete. A warning is logged at startup so users see
    // they can clean these up next time they re-run `brokk install`.
    /// DEPRECATED: use `--ollama-url` instead. The `/v1` suffix is stripped
    /// automatically. Logged at warn so existing configs keep working.
    #[arg(long, hide = true)]
    endpoint_url: Option<String>,

    /// DEPRECATED: ignored. Codex auto-detects credentials from
    /// `~/.codex/auth.json`; Ollama doesn't use an API key.
    #[arg(long, env = "BROKK_ENDPOINT_API_KEY", hide = true)]
    api_key: Option<String>,

    /// DEPRECATED: ignored. Codex is auto-detected when
    /// `~/.codex/auth.json` is present. Use `--no-codex` to opt out.
    #[arg(long, hide = true)]
    use_codex: bool,
}

impl std::fmt::Debug for Args {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        f.debug_struct("Args")
            .field("ollama_url", &self.ollama_url)
            .field("no_ollama", &self.no_ollama)
            .field("no_codex", &self.no_codex)
            .field("default_model", &self.default_model)
            .field("max_turns", &self.max_turns)
            .field("max_sessions", &self.max_sessions)
            .field("max_history_turns", &self.max_history_turns)
            .field("bifrost_binary", &self.bifrost_binary)
            // Deprecated flags omitted from Debug to avoid leaking api_key.
            .finish()
    }
}

/// Resolve the effective Ollama URL, honouring deprecated `--endpoint-url`
/// when present. Strips a trailing `/v1` since the new semantics expect
/// the bare base URL (`http://localhost:11434`), while the old flag's
/// default carried it (`http://localhost:11434/v1`). Logs a deprecation
/// warning so users notice their config is stale.
fn resolve_ollama_url(args: &Args) -> String {
    if let Some(legacy) = args.endpoint_url.as_deref() {
        let trimmed = legacy.trim_end_matches('/');
        let stripped = trimmed.strip_suffix("/v1").unwrap_or(trimmed);
        tracing::warn!(
            "--endpoint-url is deprecated; treating {legacy:?} as Ollama base URL ({stripped:?}). \
             Re-run `brokk install` (or update your editor config) to switch to --ollama-url."
        );
        return stripped.to_string();
    }
    args.ollama_url.clone()
}

/// Build the Codex backend if `~/.codex/auth.json` is present. Returns
/// `None` when Codex is disabled, the file is missing, or the file is in
/// neither `chatgpt` nor `apikey` mode (the two modes we know how to
/// route). Stale credentials are refreshed proactively so the first
/// prompt doesn't burn a 401 round-trip.
async fn build_codex_backend(no_codex: bool) -> Option<Arc<dyn LlmBackend>> {
    if no_codex {
        return None;
    }
    let mut auth = match codex_auth::read_auth_dot_json() {
        Ok(Some(a)) => a,
        Ok(None) => {
            tracing::info!(
                "no ~/.codex/auth.json found; Codex auto-discovery skipped. Run /codex-login from a session to authenticate."
            );
            return None;
        }
        Err(e) => {
            tracing::warn!("failed to read ~/.codex/auth.json: {e:#}");
            return None;
        }
    };
    if let Err(e) = codex_auth::refresh_if_stale(&mut auth).await {
        tracing::warn!("codex credential refresh failed: {e:#}");
    }
    match auth.auth_mode.as_deref() {
        Some("chatgpt") if auth.tokens.is_some() => {
            tracing::info!(
                "Codex backend enabled in ChatGPT subscription mode (Responses API on chatgpt.com)"
            );
            Some(Arc::new(codex_client::CodexClient::new()))
        }
        Some("apikey") | Some(_) => {
            // `apikey` is the documented fallback; any other auth_mode
            // (or a chatgpt mode that's missing tokens) lands here too,
            // because falling back to OPENAI_API_KEY is the only thing
            // we can sensibly do. If there's no key either, the first
            // prompt will 401 -- the user needs to re-run /codex-login.
            let key = auth.openai_api_key.clone();
            if key.is_none() {
                tracing::warn!(
                    "~/.codex/auth.json is unusable: not in chatgpt mode AND no OPENAI_API_KEY; \
                     skipping Codex backend. Run /codex-login from a session to re-authenticate."
                );
                return None;
            }
            tracing::info!(
                "Codex backend enabled in OPENAI_API_KEY mode (api.openai.com), auth_mode={:?}",
                auth.auth_mode
            );
            Some(Arc::new(llm_client::OpenAiClient::new(
                "https://api.openai.com/v1".to_string(),
                key,
            )))
        }
        None => {
            tracing::warn!(
                "~/.codex/auth.json has no auth_mode field; skipping Codex backend. \
                 Run /codex-login to refresh."
            );
            None
        }
    }
}

/// Build the Ollama chat backend (separate from discovery, which uses
/// the native `/api/tags` endpoint). Chat requests go through the
/// OpenAI-compatible `/v1/chat/completions` shim Ollama exposes, which
/// is why we reuse `OpenAiClient` here -- but we point it at `<base>/v1`
/// rather than the legacy combined CLI flag.
fn build_ollama_backend(base_url: &str, disabled: bool) -> Option<Arc<dyn LlmBackend>> {
    if disabled {
        return None;
    }
    let base = base_url.trim_end_matches('/');
    let chat_url = format!("{base}/v1");
    tracing::info!("Ollama backend enabled at {chat_url} (chat) and {base}/api/tags (discovery)");
    // Ollama doesn't require an API key for local use.
    Some(Arc::new(llm_client::OpenAiClient::new(chat_url, None)))
}

#[tokio::main]
async fn main() -> Result<()> {
    // Configure tracing to stderr only (stdout is reserved for JSON-RPC)
    tracing_subscriber::fmt()
        .with_writer(std::io::stderr)
        .with_env_filter(
            tracing_subscriber::EnvFilter::try_from_default_env()
                .unwrap_or_else(|_| tracing_subscriber::EnvFilter::new("info")),
        )
        .init();

    let args = Args::parse();

    if args.api_key.is_some() {
        tracing::warn!(
            "--api-key (env BROKK_ENDPOINT_API_KEY) is deprecated and ignored. \
             Codex credentials are read from ~/.codex/auth.json; Ollama does not use a key."
        );
    }
    if args.use_codex {
        tracing::warn!(
            "--use-codex is deprecated and ignored. Codex is auto-detected when \
             ~/.codex/auth.json exists; use --no-codex to opt out."
        );
    }
    let ollama_url = resolve_ollama_url(&args);

    let codex_backend = build_codex_backend(args.no_codex).await;
    let ollama_backend = build_ollama_backend(&ollama_url, args.no_ollama);

    let discovery_config = DiscoveryConfig {
        ollama_url: if args.no_ollama {
            None
        } else {
            Some(ollama_url)
        },
        codex_enabled: !args.no_codex,
    };

    if codex_backend.is_none() && ollama_backend.is_none() {
        tracing::warn!(
            "neither Codex nor Ollama is configured; the model picker will be empty until \
             you run /codex-login or start Ollama (then create a new session to refresh \
             discovery)"
        );
    }

    let llm: Arc<dyn LlmBackend> = Arc::new(MultiBackend::new(
        codex_backend,
        ollama_backend,
        discovery_config,
    ));

    let limits = session::SessionLimits {
        max_sessions: args.max_sessions,
        max_history_turns: args.max_history_turns,
    };
    let sessions = session::SessionStore::with_limits(args.default_model, limits);

    let max_turns = args.max_turns.max(1);
    agent::run_agent(llm, sessions, max_turns, args.bifrost_binary)
        .await
        .map_err(|e| {
            tracing::error!("agent error: {e}");
            anyhow::anyhow!("agent error: {e}")
        })
}
